export async function createPortfolioHolding(holding: InsertPortfolioHolding) {
  const db = await getDb();
  if (!db) throw new Error("Database not available");
  
  const result = await db.insert(portfolioHoldings).values(holding);
  return result;
}

export async function getPortfolioByUser(userId: number) {
  const db = await getDb();
  if (!db) return [];
  
  return await db
    .select()
    .from(portfolioHoldings)
    .where(
      and(
        eq(portfolioHoldings.userId, userId),
        eq(portfolioHoldings.status, "active")
      )
    )
    .orderBy(desc(portfolioHoldings.createdAt));
}

export async function updatePortfolioHolding(
  id: number,
  updates: Partial<InsertPortfolioHolding>
) {
  const db = await getDb();
  if (!db) throw new Error("Database not available");
  
  return await db
    .update(portfolioHoldings)
    .set(updates)
    .where(eq(portfolioHoldings.id, id));
}

export async function closePortfolioPosition(id: number) {
  const db = await getDb();
  if (!db) throw new Error("Database not available");
  
  return await db
    .update(portfolioHoldings)
    .set({ status: "closed", exitDate: new Date() })
    .where(eq(portfolioHoldings.id, id));
}

